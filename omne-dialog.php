<div id="dialog-arte" style="width: 500px;" title="Co je arteterapie?">
    <p>
        Arteterapie se opírá o výtvarný projev klienta jako o léčebný prostředek, který umožňuje přímější vyjádření emocí, 
        potlačovaných myšlenek, fantazie, postupné zvědomování vnitřních rozporů. Prožitky během tvorby se dávají 
        do souvislostí s aktuálními problémy, pomáhají při hledání jejich řešení. Je vhodná u dětí, u klientů, 
        kteří se těžko vyjadřují slovy, nebo jsou jimi naopak zahlceni.
    </p>
</div>

<div id="dialog-sur" style="width: 500px;" title="Co je SUR?">
    <p>
        SUR je systém výcviku v psychoterapii založený (1967) Doc..MUDr. Jaroslavem Skálou, PhDr. Eduardem Urbanem 
        a MUDr. Jaromírem Rubešem. Slučuje v sobě prvky hlubinně psychologické, skupinově dynamické a komunitní terapie. 
        Takto orientovaný psychoterapeut se setkává s klientem „tváří tvář“, klade důraz na vztah terapeuta a 
        klienta a na vzájemný respekt, chápe lidský vývoj jako celoživotní cestu s důležitou hodnotou hledání smyslu života, 
        má na zřeteli důležitost vztahů v dětství i v současnosti a jedinečnost osobnosti člověka. 
        Pracuje s klientem na poznání jeho vlastních vzorců chování a porozumění svým motivům. 
        Tím napomáhá ke změně postojů, hledání vhodnějších způsobů řešení a zvládání aktuálních situací vlastními silami.
    </p>
</div>

<div id="dialog-podminky" style="width: 500px;" title="Podmínky spolupráce">
    <p>
        <b>Hranice profesního vztahu:</b> vztah mezi klientem a terapeutem je vymezen prostorem, časem a účelem. Setkávají se jen v prostoru terapeutické pracovny v 
        předem dohodnutém termínu. Účelem setkání je dohodnutý kontrakt – cíl spolupráce, který společně stanovují během prvního setkání. Mimo toto vymezení se 
        terapeut s klientem nesetkávají.
    </p>
    <p>
        <b>Důvěrnost:</b> všechny informace, které od klienta získám jsou důvěrné. Nesděluji žádné informace o něm ani o průběhu konzultací a to i po ukončení kontraktu. 
        V souvislosti se zajištěním odbornosti a objektivity mám možnost konzultovat průběh terapie na supervizi, i zde však respektuji anonymitu klienta.
    </p>
