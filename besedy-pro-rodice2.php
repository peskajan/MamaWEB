            <h3 class="featurette-heading">Dost dobrý rodič</h3>
            <p class="afterlead">                 
                Chcete se ujistit o svém pojetí výchovy? Přijďte načerpat nové síly a energii k plnění 
                jedné z nejnáročnějších životních rolí „být dobrým rodičem svým dětem“. Mluvit budeme také o vhodné struktuře 
                rodinného systému a o jednotlivých rolích v ní. O tom jak se vzájemně podporovat, abychom pro dítě vytvořili 
                bezpečné a přívětivé prostředí pro jeho zdravý vývoj. Prostor bude také na vaše dotazy a vlastní zkušenosti.
            </p>

            <h3 class="featurette-heading">Jak podporovat emoční a sociální vývoj dítěte</h3>
            <p class="afterlead">                 
                Emoční a sociální dovednosti dítěte ovlivňují jeho začlenění mezi ostatní děti, jeho schopnosti komunikovat s okolím a 
                tím se výrazně podílí na celkové spokojenosti dítěte. Beseda bude zaměřená na možnosti jak u dítěte rozvíjet dovednosti 
                jako je sebedůvěra, samostatnost, komunikace, empatie, vyjadřování citů apod.
            </p>

            <h3 class="featurette-heading">Chystáme se do 1. třídy - školní zralost</h3>
            <p class="afterlead">                 
                Setkání bude věnováno významu školní zralosti pro budoucí školní úspěšnost dítěte. Zaměříme se na jednotlivé 
                oblasti důležité pro dobrý start ve škole – co by dítě před nástupem do školy mělo umět a jakým způsobem ho v tom podporovat. 
                Jak postupovat v případě, kdy se dítěti v některé oblasti nedaří, popř. při odkladu školní docházky.
            </p>

            <h3 class="featurette-heading">Rozvoj soustředění a pozornosti v předškolním věku</h3>
            <p class="afterlead">                 
                Schopnost soustředění a zacílení pozornosti je jednou z důležitých podmínek úspěšného zvládání školních nároků. 
                Ovlivňuje všechny naše psychické procesy jako je učení, paměť, vnímání, představivost... 
                Beseda bude zaměřená na konkrétní činnosti a jednoduché hry, které tuto dovednost pomáhají rozvíjet.
            </p>

            <h3 class="featurette-heading">Dětské strachy a úzkosti, obtíže se spaním</h3>
            <p class="afterlead">                 
                Beseda zaměřená na možnosti jak reagovat na různé úzkostné stavy a strachy dítěte. Jak můžeme dítěti v takové situaci pomoci. 
                Společně budeme hledat také odpovědi na otázky - co jsou to noční můry a noční děsy, jejich příčiny, jaké máme možnosti 
                jim předcházet, jak reagovat, když už se to děje.
            </p>

            <h3 class="featurette-heading">Volný čas dětí</h3>
            <p class="afterlead">                 
                V dnešní době se zvyšuje tlak na rodiče a jejich rozhodování v rámci zájmové činnosti dítěte. Rodiče často řeší otázky: 
                jak by dítě mělo trávit volný čas? je vhodné aby dítě chodilo do nějakého kroužku? jak ho vybrat? kolik jich má dítě mít? 
                Kdy s nimi začít? apod. Beseda bude zaměřená na to, co dítě předškolního věku potřebuje ve svém volném čase prožívat. 
                Jak mohou rodiče podpořit jeho zdravý vývoj.
            </p>

            <h3 class="featurette-heading">Emoce v životě dítěte</h3>
            <p class="afterlead">                 
                Máte pocit, že někdy nevíte, jak reagovat na dětský hněv? Co si počít, když dítě nejde zklidnit, utišit? 
                Zdá se Vám, že dané projevy neodpovídají konkrétní situaci? V rámci besedy budeme společně hledat odpovědi na 
                tyto a další otázky - Co jsou to emoce a jak ovlivňují život člověka? Jak v rámci emocí pracuje náš mozek? 
                Proč je důležité učit děti porozumět svým emocím, pojmenovávat je a ne je potlačovat? 
                Jak učit dítě přiměřeným způsobem vyjadřovat své emoce? Jak mírnit silné reakce dítěte, jak na ně reagovat?
            </p>

            <h3 class="featurette-heading">Komunikace v rodině</h3>
            <p class="afterlead">                 
                Beseda určená rodičům, kteří chtějí podporovat vzájemnou komunikaci v rodině, naučit své děti řešit konflikty a 
                překonávat komunikační bariéry, umět vyjádřit svůj názor přijatelným způsobem, zlepšit jejich komunikaci s dospělými i 
                vrstevníky. Mluvit budeme o konkrétních činnostech a hrách, které tyto dovednosti posilují.
            </p>

            <h3 class="featurette-heading">Jak učit dítě řešit problémy</h3>
            <p class="afterlead">                 
                V životě se nelze konfliktům zcela vyhnout, ani by to nemělo být naším cílem. Konflikty k životu patří, 
                pomáhají nám formovat a ujasňovat vlastní názory a postoje i hranice své osobnosti, stimulují náš vývoj. 
                Není tedy dobré vždy se jim jen vyhýbat, zrovna tak, jako je zbytečně vyvolávat. Této dovednosti jak řešit 
                konflikty se učíme po celý život, ale základy tvoří rodiče už v raném věku dítěte. Beseda bude zaměřená na 
                možnosti jak pomoci dítěti zvládat problematické situace, jak ho naučit řešit konflikty.
            </p>

            <h3 class="featurette-heading">„Proč se pořád perete?“</h3>
            <p class="afterlead">                 
                Řešíte doma často „sourozenecké hádky“? Přemýšlíte nad tím, co k tomu vaše děti vede a jestli to můžete nějak ovlivnit? 
                Přijďte si poslechnout a popovídat o plusech a mínusech jednotlivých pořadí narození, o úskalích sourozeneckých vztahů 
                a o možnostech jak s nimi jako rodiče můžete nakládat.
            </p>

            <h3 class="featurette-heading">„Vy jste na mě zlí!“</h3>
            <p class="afterlead">                 
                Stává se Vám, že vás toto či podobné tvrzení Vašeho dítěte „dostane do úzkých“? Přemýšlíte nad tím, co se s vaším dítětem děje, 
                že má potřebu to říkat – znamená to snad, že byste po něm neměli nic chtít? Chtěli by jste tomu více porozumět?
            </p>

            <h3 class="featurette-heading">„To víš, že tě máme rádi!“</h3>
            <p class="afterlead">                 
                V životě dítěte (i dospělého) jsou okamžiky, kdy potřebují od svých blízkých ujištění o jejich náklonnosti, 
                o jejich vztahu k nim. Zároveň tato „žádost“ může rodiče znejistit. Beseda bude zaměřená na možnosti vyjadřování 
                emocí v rodině tak, abychom podporovali pozitivní klima v rodině i sebedůvěru dětí.
            </p>

            <h3 class="featurette-heading">„Nechtějí si se mnou hrát!“</h3>
            <p class="afterlead">                 
                Vrstevnické vztahy jsou důležitou součástí života dítěte a ne vždy se dítěti daří najít si mezi ostatními dětmi „to svoje místo“. 
                Společně se zamyslíme nad tím, jak můžeme dítěti pomoci hledat si kamarády, cítit se dobře ve skupině vrstevníků ať už ve škole, 
                či v zájmových kroužcích.
            </p>

            <h3 class="featurette-heading">Změny v rodinném systému po příchodu dítěte</h3>
            <p class="afterlead">                 
                Beseda zaměřená na větší porozumění změnám, které se po narození dítěte v rodině odehrávají a možnostem, 
                jak se na tyto změny připravit a zvládat je. Mluvit budeme také o vhodné struktuře rodinného systému a o 
                jednotlivých rolích v ní. O tom jak se vzájemně podporovat, abychom pro dítě vytvořili bezpečné a přívětivé 
                prostředí a umožnili mu vytvoření zdravé citové vazby (attachment). Na co si dát pozor abychom s přijetím rodičovské 
                role zároveň nepřestali být párem. Prostor bude jednak na názory odborníků, ale také na vaše dotazy a vlastní zkušenosti.
            </p>

            <h3 class="featurette-heading">Potřeby dítěte a způsob jejich naplňování</h3>
            <p class="afterlead">                 
                Co jsou to lidské potřeby a jak jsme k jejich naplňování my i dítě geneticky vybaveni. Proč jsou pro zdravý vývoj 
                dítěte důležité a jakým způsobem je přiměřeně naplňovat. Mluvit budeme o potřebách fyzických i psychických, věnovat 
                se budeme konkrétním způsobům jejich uspokojování a možným úskalím, která se přitom mohou objevit.
            </p>

            <h3 class="featurette-heading">Styly výchovy, přístup k dítěti</h3>
            <p class="afterlead">                 
                Společné hledání odpovědí na otázky – co nás samotné ovlivňuje při výchově dítěte? Co je to temperament a 
                jak ovlivňuje výchovu? Jakým způsobem je možné dítě vychovávat? Jaká jsou úskalí jednotlivých výchovných přístupů? 
                Proč je důležitá jednotnost rodičů? Co to znamená, když se řekne důslednost ve výchově? Co doporučují odborníci? 
                Jaké jsou naše vlastní zkušenosti, co se nám daří a co nedaří?
            </p>

            <h3 class="featurette-heading">Podpora všestranného vývoje dítěte</h3>
            <p class="afterlead">                 
                V rámci vývoje dítěte se děje řada zákonitých změn, které svým výchovným přístupem můžeme podpořit, 
                ale také jim nevědomě bránit. Beseda bude zaměřená na zmapování a porozumění jednotlivým vývojovým úkolům. 
                Na uvědomění si jejich významu pro zdravý duševní vývoj dítěte a na konkrétní možnosti, jak dítěti pomoci je 
                dobře zvládnout. Věnovat se budeme také jednotlivým oblastem vývoje v předškolním věku a konkrétním činnostem, 
                které tento vývoj podporují.
            </p>

            <h3 class="featurette-heading">Podpora vývoje řeči</h3>
            <p class="afterlead">                 
                Beseda zaměřená na to, jak by měl probíhat přirozený vývoj řeči v jednotlivých jazykových rovinách. Na vnitřní a 
                vnější podmínky, které správný vývoj řeči podmiňují a zmíněny budou také možné poruchy řeči. Součástí besedy bude 
                seznámení s konkrétními činnostmi, kterými rodiče mohou podporovat komunikační dovednosti dítěte.
            </p>

            <h3 class="featurette-heading">Partnerské obtíže a dítě</h3>
            <p class="afterlead">                 
                Každý pár prochází během společného soužití různými krizovými situacemi. Některé z nich jsou dány zákonitě- vývojově tj. 
                procházejí jimi všechny páry ( společná domácnost, narození dítěte, opadnutí zamilovanosti...), některé jsou specifické 
                pro daný pár. Všechny ovšem ovlivňují nejen pár samotný, ale také jejich děti. Beseda bude zaměřená na zvládání těchto 
                krizových situací s ohledem na maximální snížení negativního dopadu na děti.
            </p>

            <h3 class="featurette-heading">Budování pevné vazby k dítěti</h3>
            <p class="afterlead">                 
                Dítě se po narození aktivně vztahuje ke „svým dospělým“ a na základě jejich odezvy se tvoří různé typy citové vazby. 
                Kvalita této vazby pak ovlivňuje celoživotní nastavení poměru důvěry a nedůvěry k okolnímu světu a lidem. 
                Beseda bude zaměřená na jednotlivé body vývoje citové vazby mezi dítětem a rodičem. Na možnosti příznivého 
                posilování i případná úskalí a negativní vlivy, které mohou bezpečnému připoutání bránit.
            </p>

            <h3 class="featurette-heading">Psychický vývoj a identita dítěte</h3>
            <p class="afterlead">                 
                Psychický vývoj dítěte postupuje po jednotlivých krocích, které se vzájemně podmiňují. V každém věkovém období na 
                dítě „čeká určitý vývojový úkol“ a způsob jeho splnění pak příznivě či nepříznivě působí na další období. 
                Naplnění vývojového úkolu a rozvoj osobnosti a identity dítěte je ovlivňováno jeho vrozenými dispozicemi a ve 
                velké míře také přístupem rodičů. Beseda bude zaměřená na porozumění vývojovým úkolům a na interakce rodiče s dítětem, 
                které tento vývoj podporují, posilují.
            </p>

            <h3 class="featurette-heading">Posilování psychické odolnosti dítěte</h3>
            <p class="afterlead">                 
                Součástí naší osobnosti, charakterových vlastností je také naše psychická odolnost – tedy schopnost zvládat a překonávat 
                různé životní obtíže. Psychická odolnost nám pomáhá brát tyto obtíže jako výzvy, možnosti změny a dalšího rozvoje 
                naší osobnosti. Malá psychická odolnost s sebou nese strach z nových věcí a pocity bezmoci. Vývoj psychické 
                odolnosti začíná v raném dětství a podílí se na něm kromě vrozených dispozic především přístup rodičů. 
                Beseda bude zaměřená na postup vývoje a zdroje psychické odolnosti v jednotlivých věkových obdobích a na možnosti 
                její podpory ze strany rodičů.
            </p>

            <h3 class="featurette-heading">Hranice a jejich důležitost pro další vývoj dítěte</h3>
            <p class="afterlead">                 
                Nastavení hranic ve výchově úzce souvisí s celkovým výchovným stylem rodičů na škále od direktivního přístupu k benevolentnímu. 
                Vymezení přiměřených hranic je pro dítě a jeho další vývoj velmi důležité – souvisí to s celkovým naplňováním potřeb dítěte. 
                Zároveň dítě potřebuje cítit lásku a náklonnost rodičů. Beseda bude zaměřená na možnosti jak vhodné a přiměřené hranice 
                nastavovat, jak zároveň budovat citovou vazbu s dítětem a podporovat jeho samostatnost a zodpovědnost
            </p>
